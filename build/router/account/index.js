"use strict";
var Router = require('express').Router;
var verifyToken = require('../../lib/auth').verifyToken;
var _a = require('./account'), register = _a.register, unregister = _a.unregister, login = _a.login, logout = _a.logout, findid = _a.findid, resetpw = _a.resetpw;
var router = Router();
router.post('/register', register);
router.post('/unregister', verifyToken, unregister);
router.post('/login', login);
router.post('/logout', verifyToken, logout);
router.post('/findid', findid);
router.post('/resetpw', resetpw);
module.exports = router;
